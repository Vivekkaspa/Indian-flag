#This code snippet demonstrates the Indian flag design by using turtle programming.

import turtle
t=turtle.Turtle()
t.forward(500)
t.left(90)
t.forward(150)
t.left(90)
t.forward(500)
t.left(90)
t.forward(150)
t.begin_fill()
t.fillcolor("green")
t.forward(150)
t.left(90)
t.forward(500)
t.left(90)
t.forward(150)
t.end_fill()
t.forward(150)
t.begin_fill()
t.fillcolor("orange")
t.forward(150)
t.left(90)
t.forward(500)
t.left(90)
t.forward(150)
t.end_fill()
t.forward(150)
t.left(90)
t.forward(250)
t.color("blue")
t.circle(75)
t.up()
t.goto(250,75)
t.down()
for i in range(24):
	t.forward(75)
	t.bk(75)
	t.left(15)
